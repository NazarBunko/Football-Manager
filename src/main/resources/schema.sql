CREATE TABLE PLAYER (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(255) NOT NULL,
    AGE INT NOT NULL,
    POSITION VARCHAR(50) NOT NULL,
    EXPERIENCE INT NOT NULL,
    TEAM_ID INT,
    CONSTRAINT FK_TEAM FOREIGN KEY (TEAM_ID) REFERENCES TEAM(ID)
);

