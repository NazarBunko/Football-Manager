CREATE TABLE TEAM (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(255) NOT NULL,
    MONEY NUMERIC(10, 2) DEFAULT 0.00,
    PERCENT NUMERIC(5, 2) DEFAULT 0.00
);

